#include "libft.h"

// \t (9), \n (10), \v (11), \f (12), \r (13), ' ' (32)

int	ft_isspace(int s)
{
	if (s == '\f' || s == ' ' || s == '\n')
		return (1);
	if (s == '\r' || s == '\t' || s == '\v')
		return (1);
	return (0);
}
